//Java Program to Check Whether a Number is Even or Odd
package assignment;

public class Example05 {
	
	public static void main(String[] args)
	{
		byte a = 8;
		
		if(a%2==0)
		{
			System.out.println("It is an even number.");
		}
		else
		{
			System.out.println("It is an odd number.");
		}
	}

}

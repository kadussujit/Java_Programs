//Java Program to Display Alphabets (A to Z) using loop
// ASCII value of Uppercase A=65 to Z=90
// ASCII value of Lowercase a=97 to z=122
package assignment;

public class Example18Approach1 {

	public static void main(String[] args) {

//In ASCII code system, character A stands at 65 to Z at 90
//Also, a at 97 to z at 122
		
		for(char i=65; i<=90;i++)
		{
			System.out.println(i);
		}

	}

}
